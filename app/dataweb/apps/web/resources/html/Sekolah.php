</div>

<div id="form">
<fieldset class="fieldset">
<legend>Identitas Sekolah</legend>
<div>Untuk melihat data sekolah. Data pada bagian ini hanya bisa diubah melalui <a href="http://vervalsp.data.kemdikbud.go.id" target="_blank">http://vervalsp.data.kemdikbud.go.id</a>.</div>
</fieldset>

<fieldset class="fieldset">
<legend>Lokasi Sekolah</legend>
<div><table class="table"><tr><td valign="top"><label>RT</label></td><td valign="top">RT alamat sekolah. Hanya isikan angka. Sebagai contoh, isikan <strong>7</strong>, bukan <strong>007</strong> atau <strong>VII</strong>.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>RW</label></td><td valign="top">RW alamat sekolah. Hanya isikan angka. Sebagai contoh, isikan <strong>7</strong>, bukan <strong>007</strong> atau <strong>VII</strong>.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Nama dusun</label></td><td valign="top">Nama dusun lokasi sekolah. Sebagai contoh, apabila sekolah terletak pada Dusun Merak, maka dapat diisi dengan <strong>Merak</strong>.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Kode Pos</label></td><td valign="top">Kode pos sesuai lokasi sekolah.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Lintang</label></td><td valign="top">Posisi astronomis sekolah berdasarkan garis lintang (<em>latitude</em>) dalam format derajat. Hanya bisa diubah melalui <a href="http://vervalsp.data.kemdikbud.go.id" target="_blank">http://vervalsp.data.kemdikbud.go.id</a>.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Bujur</label></td><td valign="top">Posisi astronomis sekolah berdasarkan garis bujur (<em>longitude</em>) dalam format derajat. Hanya bisa diubah melalui <a href="http://vervalsp.data.kemdikbud.go.id" target="_blank">http://vervalsp.data.kemdikbud.go.id</a>.</td></tr></table></div>
</fieldset>

<fieldset class="fieldset">
<legend>Data Pelengkap</legend>
<div><table class="table"><tr><td valign="top"><label>Kebutuhan khusus dilayani</label></td><td valign="top">Ragam kebutuhan khusus yang juga dilayani oleh sekolah. Opsi yang tersedia dapat dipilih lebih dari satu. Khusus untuk kebutuhan khusus grahita ringan dan grahita sedang hanya boleh dipilih salah satu. Daksa ringan dan sedang juga hanya bisa dipilih salah satu. Opsi ini hanya bisa dipilih jika sebelumnya data Program Inklusi telah diisikan.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>SK pendirian sekolah</label></td><td valign="top">Nomor SK pendirian sekolah. Hanya bisa diubah melalui <a href="http://vervalsp.data.kemdikbud.go.id" target="_blank">http://vervalsp.data.kemdikbud.go.id</a>.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Tanggal SK pendirian</label></td><td valign="top">Tanggal SK pendirian sekolah. Hanya bisa diubah melalui <a href="http://vervalsp.data.kemdikbud.go.id" target="_blank">http://vervalsp.data.kemdikbud.go.id</a>.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Status kepemilikan</label></td><td valign="top">Pihak yang memiliki sekolah.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Yayasan</label></td><td valign="top">Yayasan yang menaungi sekolah. Opsi ini muncul jika data yayasan pada Data Rinci Sekolah telah diisi. Khusus sekolah swasta.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>SK izin operasional</label></td><td valign="top">Nomor SK izin beroperasinya sekolah. Hanya bisa diubah melalui <a href="http://vervalsp.data.kemdikbud.go.id" target="_blank">http://vervalsp.data.kemdikbud.go.id</a>.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Tanggal SK izin operasional</label></td><td valign="top">Tanggal SK operasional sekolah. Hanya bisa diubah melalui <a href="http://vervalsp.data.kemdikbud.go.id" target="_blank">http://vervalsp.data.kemdikbud.go.id</a>.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Nomor rekening</label></td><td valign="top">Nomor rekening BOS/BOP milik sekolah. Hanya isikan angka.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Nama bank</label></td><td valign="top">Nama bank tempat rekening BOS/BOP milik sekolah didaftarkan.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Cabang KCP/unit</label></td><td valign="top">Diisi nama kantor cabang/unit/pembantu dari bank tempat rekening BOS/BOP milik sekolah didaftarkan.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Rekening atas nama</label></td><td valign="top">Diisi nama pemilik sesuai dengan yang tertera pada buku rekening BOS/BOP milik sekolah.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>MBS</label></td><td valign="top">MBS adalah singkatan dari Manajemen Berbasis Sekolah. Pilih salah satu, sekolah telah melaksanakan (<strong>Ya</strong>) atau tidak melaksanakan (<strong>Tidak</strong>).</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Luas Tanah Milik (m<sup>2</sup>)</label></td><td valign="top">Luas tanah milik sekolah. Satuan yang dipakai adalah meter per segi.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Luas Tanah Bukan Milik (m<sup>2</sup>)</label></td><td valign="top">Luas tanah yang digunakan oleh sekolah, namun bukan milik sekolah (misalnya menumpang pada pihak-pihak tertentu). Satuan yang dipakai adalah meter per segi.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Nama Wajib Pajak</label></td><td valign="top">Nama yang tercantum pada kartu NPWP milik sekolah.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>NPWP</label></td><td valign="top">Nomor Pokok Wajib Pajak milik sekolah.</td></tr></table></div>
