<div id="deskripsi">
<p>Tabel ini untuk melengkapi data pendidik maupun tenaga kependidikan (PTK) di sekolah.</p>
</div>

<div id="form">
<fieldset class="fieldset">
<legend>Identitas</legend>
<div><table class="table"><tr><td valign="top"><label>Nama (Tanpa Gelar)</label></td><td valign="top">Nama PTK sesuai dokumen resmi yang berlaku, tanpa mencantumkan gelar (baik gelar sosial maupun gelar akademik). Hanya bisa diubah melalui <a href="http://vervalptk.data.kemdikbud.go.id" target="_blank">http://vervalptk.data.kemdikbud.go.id</a>.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>NIK / No.Passport (Untuk WNA)</label></td><td valign="top">Bagi WNI, diisi dengan NIK yang tertera pada Kartu Keluarga (KK) atau KTP. Sedangkan bagi WNA diisi dengan nomor paspor yang berlaku.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Jenis kelamin</label></td><td valign="top">Jenis kelamin PTK.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Tempat lahir</label></td><td valign="top">Tempat lahir PTK sesuai dokumen resmi yang berlaku.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Tanggal lahir</label></td><td valign="top">Tanggal lahir PTK sesuai dokumen resmi yang berlaku. Hanya bisa diubah melalui <a href="http://vervalptk.data.kemdikbud.go.id" target="_blank">http://vervalptk.data.kemdikbud.go.id</a>.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Nama ibu kandung</label></td><td valign="top">Nama ibu kandung PTK sesuai dokumen resmi yang berlaku. Hanya bisa diubah melalui <a href="http://vervalptk.data.kemdikbud.go.id" target="_blank">http://vervalptk.data.kemdikbud.go.id</a>.</td></tr></table></div></fieldset>

<fieldset class="fieldset">
<legend>Data Pribadi</legend>
<div><table class="table"><tr><td valign="top"><label>Alamat jalan</label></td><td valign="top">Jalur tempat tinggal PTK, terdiri atas gang, kompleks, blok, nomor rumah, dan sebagainya yang relevan selain informasi yang diminta oleh kolom-kolom selanjutnya. Sebagai contoh, PTK tinggal di sebuah kompleks perumahan Griya Adam yang berada pada Jalan Kemanggisan, dengan nomor rumah 4-C, di lingkungan RT 005 dan RW 011, Dusun Cempaka, Desa Salatiga. Maka dapat diisi dengan <strong>Jl. Kemanggisan, Komp. Griya Adam, No. 4-C</strong>.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>RT</label></td><td valign="top">RT alamat PTK. Dari contoh di atas, misalnya dapat diisi dengan angka <strong>5</strong>.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>RW</label></td><td valign="top">RW alamat PTK. Dari contoh di atas, misalnya dapat diisi dengan angka <strong>11</strong>.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Nama dusun</label></td><td valign="top">Nama dusun alamat PTK. Dari contoh di atas, misalnya dapat diisi dengan <strong>Cempaka</strong>.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Desa/Kelurahan</label></td><td valign="top">Nama desa atau kelurahan alamat PTK. Dari contoh di atas, dapat diisi dengan <strong>Salatiga</strong>.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Kecamatan</label></td><td valign="top">Kecamatan alamat PTK. Ketikkan nama kecamatan untuk menyaring referensi kecamatan, lalu akan muncul nama kecamatan dan kabupatennya. Pilih kecamatan yang sesuai.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Kode pos</label></td><td valign="top">Kode pos alamat PTK.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Lintang</label></td><td valign="top">Diisi dengan titik koordinat lintang (latitude) yang muncul pada peta dilokasi PTK tersebut tinggal.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Bujur</label></td><td valign="top">Diisi dengan titik koordinas Bujur (longitude) yang muncul pada peta dilokasi PTK tersebut tinggal.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Agama &amp; Kepercayaan</label></td><td valign="top">	Agama atau kepercayaan yang dianut oleh PTK. Apabila PTK adalah penghayat kepercayaan (misalnya pada daerah tertentu yang masih memiliki penganut kepercayaan), dapat memilih opsi <strong>Kepercayaan kpd Tuhan YME</strong>.	</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>NPWP</label></td><td valign="top">Nomor Pokok Wajib Pajak milik PTK (jika memiliki).</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Nama Wajib Pajak</label></td><td valign="top">Nama yang tercantum pada data NPWP milik PTK (jika memiliki). Dapat dilihat pada kartu NPWP milik PTK terkait.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Kewarganegaraan</label></td><td valign="top">Kewarganegaraan PTK.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Status perkawinan</label></td><td valign="top">Status perkawinan PTK.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Nama suami/istri</label></td><td valign="top">Nama suami/istri PTK yang ditanggung (jika ada), tanpa mencantumkan gelar (baik gelar sosial maupun gelar akademik).</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>NIP suami/istri</label></td><td valign="top">NIP suami/istri PTK yang ditanggung apabila suami/istri PTK tersebut bekerja sebagai PNS (jika ada).</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Pekerjaan suami/istri</label></td><td valign="top">Pekerjaan utama suami/istri PTK yang ditanggung (jika ada). Apabila istri sebagai ibu rumah tangga, dapat dipilih <strong>Lainnya</strong>.</td></tr></table></div>
</fieldset>

<fieldset class="fieldset">
<legend>Kepegawaian</legend>
<div><table class="table"><tr><td valign="top"><label>Status kepegawaian</label></td><td valign="top">Status kepegawaian PTK saat ini. Isian ini harus sama antara sekolah induk dan bukan induk. Apabila di sekolah induk dinyatakan sebagai PNS, maka di sekolah bukan induk juga harus PNS, walaupun sekolah tersebut adalah sekolah swasta.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>NIP</label></td><td valign="top">Nomor Induk Pegawai milik PTK sesuai yang dikeluarkan oleh BKN dalam format 18 digit (NIP baru). Hanya untuk PNS.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>NIY/NIGK</label></td><td valign="top">Nomor Induk Yayasan (NIY) / Nomor Induk Guru Kontrak (NIGK) bagi PTK yang bertugas di sekolah swasta. Kosongkan untuk sekolah negeri.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>NUPTK</label></td><td valign="top">Nomor Unik Pendidik dan Tenaga Kependidikan (jika memiliki).</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Jenis PTK</label></td><td valign="top">Jenis PTK sesuai dengan tugas utamanya di sekolah.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>SK Pengangkatan</label></td><td valign="top">Nomor SK pengangkatan sesuai dengan status kepegawaian yang telah dipilih sebelumnya. Apabila status kepegawaian dipilih PNS, maka diisi nomor SK pengangkatan sebagai PNS.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>TMT Pengangkatan</label></td><td valign="top">Tanggal SK pengangkatan sesuai dengan status kepegawaian yang telah dipilih sebelumnya.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Lembaga pengangkat</label></td><td valign="top">Lembaga yang mengangkat PTK sesuai dengan SK pengangkatannya.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>SK CPNS</label></td><td valign="top">Nomor SK CPNS apabila PTK adalah CPNS atau PNS. Kosongkan selain itu.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>TMT CPNS</label></td><td valign="top">Diisi tanggal mulai bertugas sebagai CPNS. Kosongkan bila bukan CPNS atau PNS.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>TMT PNS</label></td><td valign="top">Tanggal mulai bertugas sebagai PNS. Kosongkan bila bukan PNS.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Pangkat/Golongan</label></td><td valign="top">Pangkat dan golongan PTK (terbaru). Kosongkan bila bukan PNS.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Sumber gaji</label></td><td valign="top">Sumber gaji PTK.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Kartu Pegawai</label></td><td valign="top">Nomor kartu pegawai (KARPEG) bagi PNS. Isikan tanpa spasi.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Kartu Istri (KARIS) /Kartu Suami (KARSU)</label></td><td valign="top">Apabila PTK sebagai suami, isikan nomor Kartu Istri (KARIS). Apabila PTK sebagai istri, isikan nomor Kartu Suami (KARSU).</td></tr></table></div>
</fieldset>

<fieldset class="fieldset">
<legend>Kompetensi Khusus</legend>
<div><table class="table"><tr><td valign="top"><label>Punya lisensi kepala sekolah</label></td><td valign="top">Kepemilikikan lisensi kepala sekolah, dibuktikan adanya sertifikat lulus diklat pelatihan kepala sekolah.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Keahlian laboratorium</label></td><td valign="top">Spesifikasi keahlian laboratorium yang dimiliki oleh PTK, dibuktikan dengan adanya sertifikat sebagai laboran.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Mampu menangani keb.khusus</label></td><td valign="top">Kemampuan khusus yang dapat ditangani oleh PTK. Dapat dipilih lebih dari satu.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Keahlian Braille</label></td><td valign="top">Penguasaan huruf Braille oleh PTK.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Keahlian bahasa isyarat</label></td><td valign="top">Penguasaan bahasa isyarat oleh PTK.</td></tr></table></div>
</fieldset>

<fieldset class="fieldset">
<legend>Kontak</legend>
<div><table class="table"><tr><td valign="top"><label>Nomor telepon rumah</label></td><td valign="top">Diisi nomor telepon rumah PTK (jika memiliki) dengan format &lt;kode area&gt;-&lt;nomor telepon&gt;. Contoh: <strong>021-775577</strong>.</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Nomor HP</label></td><td valign="top">Diisi nomor telepon selular (ponsel) PTK yang masih aktif/dapat dihubungi (jika memiliki).</td></tr></table></div>
<div><table class="table"><tr><td valign="top"><label>Email</label></td><td valign="top">Diisi alamat surat elektronik (surel) PTK yang masih aktif/dapat dihubungi (jika memiliki).</td></tr></table></div>
</fieldset>

<fieldset class="fieldset">
<legend>Bank (Diisi oleh Ditjen GTK)</legend>
<div>Untuk melihat data rekening bank milik PTK. Data pada bagian ini diisi oleh Direktorat Jenderal Guru dan Tenaga Kependidikan.</div>
